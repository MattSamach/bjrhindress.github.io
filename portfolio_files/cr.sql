CREATE TABLE client (
    ClientNo        VARCHAR2(5),
    ClientName      VARCHAR2(30),
    Street          VARCHAR2(30),
    City            VARCHAR2(20),
    State           CHAR(2),
    Zipcode         CHAR(5),
    WebAddress      VARCHAR2(30),
    Contact_Fname   VARCHAR2(20),
    Contact_Lname   VARCHAR2(20),
    Phone           VARCHAR2(12),
    Email           VARCHAR2(30),
    CONSTRAINT client_Clientno_pk PRIMARY KEY ( Clientno )
);

CREATE TABLE employee (
    Empno          CHAR(5),
    Fname          VARCHAR2(20),
    Lname          VARCHAR2(20),
    Position       VARCHAR2(20),
    Phone          VARCHAR2(12),
    Email          VARCHAR2(30),
    Dob            DATE,
    Gender         VARCHAR2(2),
    Salary         NUMBER(8,2),
    Hiredate       DATE DEFAULT SYSDATE,
    Outno       CHAR(4),
    Supervisorno   CHAR(5),
    CONSTRAINT employee_Empno_pk PRIMARY KEY ( Empno ),
    CONSTRAINT employee_Supervisorno_fk FOREIGN KEY ( Supervisorno )
        REFERENCES employee ( Empno ),
    CONSTRAINT employee_Salary_ck CHECK ( Salary > 0 ),
    CONSTRAINT employee_Email_ck CHECK ( Email like '%@%.%' )
);

CREATE TABLE outlet (
    Outno    CHAR(4),
    Street      VARCHAR2(30),
    City        VARCHAR2(20),
    State       CHAR(2),
    Zipcode     CHAR(5),
    Phone       VARCHAR2(12),
    Managerno   CHAR(5),
    CONSTRAINT outlet_Outletno_pk PRIMARY KEY ( Outno ),
    CONSTRAINT outlet_Managerno_fk FOREIGN KEY ( Managerno )
        REFERENCES employee ( Empno )
);

ALTER TABLE employee
    ADD CONSTRAINT employee_Outno_fk FOREIGN KEY ( Outno )
        REFERENCES outlet ( Outno );

CREATE TABLE vehicle (
    LicenseNo        VARCHAR2(10),
    Make             VARCHAR2(20),
    Model            VARCHAR2(20),
    Color            VARCHAR2(10),
    Year     NUMBER(4),
    NoDoors          NUMBER,
    Capacity         NUMBER,
    DailyRate        NUMBER(7,2),
    InspectionDate   DATE,
    Outno         CHAR(4),    
    CONSTRAINT vehicle_Licenseno_pk PRIMARY KEY ( Licenseno ),
    CONSTRAINT vehicle_Outletno_fk FOREIGN KEY ( Outno )
        REFERENCES outlet ( Outno ),
    CONSTRAINT vehicle_Age_ck CHECK ( 2018-Year <= 10 )
);

CREATE TABLE ragreement (
    RentalNo        CHAR(5),
    StartDate       DATE,
    ReturnDate      DATE,
    MileageBefore   NUMBER(7,1),
    MileageAfter    NUMBER(7,1),
    InsuranceType   VARCHAR2(10),
    ClientNo        VARCHAR2(5),
    LicenseNo       VARCHAR2(10),
    CONSTRAINT ragreement_Rentalno_pk PRIMARY KEY ( Rentalno ),
    CONSTRAINT ragreement_Clientno_fk FOREIGN KEY ( Clientno )
        REFERENCES client ( Clientno ),
    CONSTRAINT ragreement_Licenseno_fk FOREIGN KEY ( Licenseno )
        REFERENCES vehicle ( Licenseno ),
    CONSTRAINT ragreement_Returndate_ck CHECK(Returndate >= Startdate) 
);

CREATE TABLE faultreport (
    ReportNum      CHAR(5),
    DateChecked   DATE,
    Comments      VARCHAR2(200),
    Empno         CHAR(5),
    LicenseNo     VARCHAR2(10),
    RentalNo      CHAR(5),
    CONSTRAINT faultreport_Reportno_pk PRIMARY KEY ( Reportnum ),
    CONSTRAINT faultreport_Empno_fk FOREIGN KEY ( Empno )
        REFERENCES employee ( Empno ),
    CONSTRAINT faultreport_licenseno_fk FOREIGN KEY ( Licenseno )
        REFERENCES vehicle ( Licenseno ),
    CONSTRAINT faultreport_rentalno_fk FOREIGN KEY ( Rentalno )
        REFERENCES ragreement ( Rentalno )
);

